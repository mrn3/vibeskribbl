function normalizeCustomWord(w: string) {
  return w.trim().replace(/\s+/g, " ");
}

export function parseCustomWords(text: string) {
  return text
    .split(",")
    .map((s) => normalizeCustomWord(s))
    .filter((w) => w.length > 0 && w.length <= 32);
}

export function pickWords(
  count: number,
  settings: { customWordsOnly: boolean; customWords: string[] }
) {
  const pool = settings.customWordsOnly
    ? settings.customWords
    : settings.customWords.length > 0
      ? [...DEFAULT_WORDS, ...settings.customWords]
      : DEFAULT_WORDS;

  const uniq = Array.from(new Set(pool.map((w) => w.toLowerCase())));
  if (uniq.length === 0) return [];

  const out: string[] = [];
  while (out.length < Math.min(count, uniq.length)) {
    const w = uniq[Math.floor(Math.random() * uniq.length)];
    if (!out.includes(w)) out.push(w);
  }
  return out;
}
